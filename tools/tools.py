def unnorm_coords(coords1, h, w):
    coords1x, coords1y = torch.split(coords1, 1, dim=1)
    coords1x = (coords1x + 1) / 2 * (w - 1)
    coords1y = (coords1y + 1) / 2 * (h - 1)
    return torch.cat([coords1x, coords1y], dim=1)

